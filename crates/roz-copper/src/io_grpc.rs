/// Convert a gRPC [`PoseBatch`] into a [`SensorFrame`].
fn pose_batch_to_sensor_frame(batch: &proto::PoseBatch) -> SensorFrame {
    let entities: Vec<EntityState> = batch.poses.iter().map(entity_pose_to_state).collect();

    #[expect(
        clippy::cast_possible_truncation,
        reason = "sim_time seconds to nanos; truncation is acceptable for simulation timestamps"
    )]
    let sim_time_ns = (batch.sim_time * 1_000_000_000.0) as i64;

    SensorFrame {
        entities,
        joint_positions: Vec::new(),
        joint_velocities: Vec::new(),
        sim_time_ns,
        wrench: None,
        contact: None,
        frame_snapshot_input: pose_batch_to_snapshot_input(batch, sim_time_ns),
    }
}

fn pose_batch_to_snapshot_input(batch: &proto::PoseBatch, sim_time_ns: i64) -> FrameSnapshotInput {
    let timestamp_ns = u64::try_from(sim_time_ns).unwrap_or(0);
    let dynamic_transforms = batch
        .poses
        .iter()
        .filter_map(|pose| {
            let transform = pose.transform.as_ref()?;
            let frame_id = canonical_frame_id_from_pose_path(&pose.path);
            if frame_id.is_empty() {
                return None;
            }
            Some(TimestampedTransform {
                frame_id,
                parent_id: Some("world".into()),
                transform: Transform3D {
                    translation: [transform.x, transform.y, transform.z],
                    rotation: [transform.qw, transform.qx, transform.qy, transform.qz],
                    timestamp_ns,
                },
                freshness: FreshnessState::Fresh,
                source: FrameSource::Dynamic,
            })
        })
        .collect();

    FrameSnapshotInput {
        dynamic_transforms,
        ..FrameSnapshotInput::default()
    }
}

fn canonical_frame_id_from_pose_path(path: &str) -> String {
    path.rsplit('/')
        .find(|segment| !segment.is_empty())
        .unwrap_or(path)
        .to_string()
}

/// Convert a single gRPC [`EntityPose`] into an [`EntityState`].
fn entity_pose_to_state(ep: &proto::EntityPose) -> EntityState {
    let (position, orientation) = ep.transform.as_ref().map_or((None, None), |t| {
        (
            Some([t.x, t.y, t.z]),
            Some([t.qw, t.qx, t.qy, t.qz]), // roz convention: [w, x, y, z]
        )
    });

    EntityState {
        id: ep.path.clone(),
        kind: "gazebo_model".to_owned(),
        position,
        orientation,
        velocity: None,
        properties: HashMap::new(),
        timestamp_ns: None,
        frame_id: "world".to_owned(),
        last_observed_ns: None,
        observation_confidence: 0.0,
    }
}

