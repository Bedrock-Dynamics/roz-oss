/// Streams [`PoseBatch`] from the substrate-sim-bridge and buffers the latest
/// [`SensorFrame`] for the controller loop to consume each tick.
///
/// A background tokio task maintains a persistent gRPC stream.  The controller
/// calls [`try_recv`] at 100 Hz; it returns `Some` when new data has arrived
/// since the last call, `None` otherwise.
///
/// [`try_recv`]: SensorSource::try_recv
pub struct GrpcSensorSource {
    latest: Arc<parking_lot::Mutex<Option<SensorFrame>>>,
    _handle: tokio::task::JoinHandle<()>,
}

impl GrpcSensorSource {
    /// Connect to the bridge at `bridge_url` and begin streaming poses.
    ///
    /// # Errors
    ///
    /// Returns an error if the initial gRPC connection fails.
    pub async fn connect(bridge_url: &str) -> anyhow::Result<Self> {
        let channel = tonic::transport::Channel::from_shared(bridge_url.to_string())?
            .connect()
            .await?;

        let latest: Arc<parking_lot::Mutex<Option<SensorFrame>>> = Arc::new(parking_lot::Mutex::new(None));
        let latest_writer = Arc::clone(&latest);

        let handle = tokio::spawn(async move {
            Self::stream_loop(channel, latest_writer).await;
        });

        Ok(Self {
            latest,
            _handle: handle,
        })
    }

    /// Background loop: opens a `StreamPoses` stream and buffers each batch.
    ///
    /// If the stream breaks, logs a warning and exits.  A production version
    /// would reconnect with exponential backoff.
    async fn stream_loop(channel: tonic::transport::Channel, latest: Arc<parking_lot::Mutex<Option<SensorFrame>>>) {
        let mut client = SceneServiceClient::new(channel);

        let request = proto::StreamPosesRequest {
            world_name: String::new(),
            entity_filter: vec![],
        };

        let stream = match client.stream_poses(request).await {
            Ok(response) => response.into_inner(),
            Err(e) => {
                tracing::error!(error = %e, "StreamPoses RPC failed");
                return;
            }
        };

        tokio::pin!(stream);

        while let Some(result) = stream.next().await {
            match result {
                Ok(batch) => {
                    let frame = pose_batch_to_sensor_frame(&batch);
                    *latest.lock() = Some(frame);
                }
                Err(e) => {
                    tracing::warn!(error = %e, "StreamPoses stream error, exiting");
                    return;
                }
            }
        }

        tracing::info!("StreamPoses stream ended");
    }
}

impl SensorSource for GrpcSensorSource {
    fn try_recv(&mut self) -> Option<SensorFrame> {
        self.latest.lock().take()
    }
}

